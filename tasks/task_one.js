function js_style (){
    let changeStyle = document.getElementById('text') //i targetted the id:text, and assigned it to the variable changeStyle
    changeStyle.style.color = 'red' //i added color red to the changeStyle variable by using the style element, it changed the color to red
    changeStyle.style.fontSize = '30px' //changed the font size to 30px
    changeStyle.style.fontFamily = 'vedana' //changed the font family
}

